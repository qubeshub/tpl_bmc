      <h1>QUBES</h1>
      <h3>Quantitative Undergraduate Biology Education and Synthesis</h3>
      <p>The QUBES platform, a BioQUEST project, is a social cyberinfrastructure that provides an open and inclusive virtual space for sharing STEM classroom activities and resources, discussing teaching and the adaptation of educational materials to specific institutional contexts, and working together to develop new ideas and insights that contribute to STEM education reform.</p>
      <p>The platform is in service of BioQUEST’s 35-year mission to build a transformative, collaborative community that empowers Life Science educators to drive innovation in STEM education for all students.</p>
