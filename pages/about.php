  <section class="about">
    <h6 id="about">With support from the National Science Foundation, the QUBES project was launched in 2014 to address challenges in quantitative biology education. The cyberinfrastructure and its leadership team provided logistical, intellectual, and community support for innovative quantitative biology education projects and the extended community of instructors seeking resources.</h6>
    <h6>As NSF funding for the QUBES project came to an end in August of 2021, the QUBES infrastructure officially moved under the management of BioQUEST, and became part of BioQUEST's mission. This shift had been evolving through the final years of the project and ensures sustainability for the QUBES platform.</h6>
    <h6>It also provides exciting opportunities for new collaborations and professional development that leverage BioQUEST's long track record of community building with the QUBES platform’s broad reach and functionality. For more information about QUBES' history, visit our <a href="/qubeshistory">archived page</a>.</h6>
