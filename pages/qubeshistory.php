    <div class="mission-statement">
      <h1>QUBES</h1>
      <h3>Quantitative Undergraduate Biology Education and Synthesis</h3>
      <h4>QUBES is a community of individuals and organizations committed to accelerating STEM education reform. Supported by our social-cyberinfrastructure and commitment to teaching quantitative skills, we work to make sure that our teaching is effective, open, accessible, equitable, and inclusive.</h4>
    </div>
