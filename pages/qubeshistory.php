  <section class="about">
    <div class="pane-1">
      <div class="content1">
        <h6 id="about">The QUBES organization was launched in 2014 to address challenges in quantitative biology education and now provides logistical, intellectual, and community support for innovative quantitative biology education projects and the extended community of instructors seeking resources.</h6>
        <img alt="QUBES logo" class="img-logo" src="/app/site/media/images/qubes_logo_web_200x200_transp.png" />
      </div>
    </div>

    <div class="pane-2">
      <p>QUBES emphasizes practices that promote scholarly teaching practices, including use of evidence based pedagogies, thoughtful reflection on implementation results, and sharing outcomes with the community. QUBES embraces diversity in the community - diversity of our partners as well as the faculty we serve (career stages, institution types, and disciplinary backgrounds).</p>

      <p>We have adopted a virtual synthesis center model to serve that community, providing a cyberinfrastructure and associated social infrastructure to support collaborations between producers and consumers of educational reform resources. Our cyberinfrastructure was purposefully designed to offer inclusive access to professional development and instructional resources so that all faculty regardless of local constraints could participate in the quantitative education community.</p>

      <p>Via the QUBES platform, partner projects have been able to leverage the scientific collaboration infrastructure and community resources to address hard problems in biology education. Faculty in the QUBES community have access to partner project resources, computational tools, and professional development programs to help them use resources more effectively.</p>

      <p>We are proud of the diversity of our leadership team in representing a variety of institutional types, locations, departmental affiliations, and disciplinary training, in addition to gender and ethnic diversity. This diversity is key to our success in working with those we serve.</p>
    </div>
