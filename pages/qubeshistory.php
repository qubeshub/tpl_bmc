        <div class="aof-description aof-team focus">
          <h4>Team QUBES</h4>

          <p>The QUBES Team includes mathematicians, biologists, professional developers, and evaluation experts who came together through our work on various projects in quantitative biology education. The QUBES Team is distributed across the country and collaborates online using the QUBES cyberinfrastructure to manage the project.</p>
        </div>

        <div class="aof-description aof-business">
          <h4>Project Management</h4>

          <p>The QUBES PIs are based at University of Pittsburgh, Radford University, BioQUEST, College of William and Mary and Bates College.</p>
        </div>

        <div class="aof-description aof-partners">
          <h4>Consortium Partnerships</h4>

          <p>The Partners team focuses on developing partnerships that positively grow the QUBES community. We oversee our social media presence and many of the organized minisymposia across our partner professional societies that showcase QUBES community work or bring together influential researchers to help a community examine a challenge space. Our team help partners advertise their education work, connect it to our QUBES community, and virtually facilitate communities through our group websites. We also help co-develop education and dissemination plans for research grants.</p>
        </div>

        <div class="aof-description aof-fmn">
          <h4>Professional Development</h4>

          <p>The Professional Development team provides access for all faculty to high-quality instructional mentorship and resources via a novel development model we call Faculty Mentoring Networks (FMNs). FMNs are online, sustained peer interactions around particular instructional materials or challenges in quantitative biology.</p>
        </div>

        <div class="aof-description aof-site">
          <h4>CyberInfrastructure</h4>

          <p>Cyberinfrastructure Team&rsquo;s responsibility is to provide technical support, assess user and partner needs, and provide web development for QUBES. The QUBES cyberinfrastructure is based on the <a href="https://hubzero.org">HubZero platform</a>, an open source content management system with the ability to build dynamic websites, host software, share and publish resources, collaborate, and build communities in a single web-based ecosystem.</p>
        </div>

        <div class="aof-description aof-evaluation">
          <h4>Evaluation</h4>

          <p>The National Institute for STEM Evaluation and Research (NISER) serves as an external evaluator for the entirety of the QUBES project. NISER takes a utilization-focused approached to evaluations by collecting quantitative and qualitative data and reporting to the QUBES leadership team formatively to assist in making data-based decisions. During the 2018-2019 academic year, NISER will continue to assist the Assessment Area of Focus team by gathering metrics for the website, open education resources, and collaborations of QUBES hub members.</p>
        </div>
